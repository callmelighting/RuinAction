    private void createCreature()
    {
        player = Instantiate(playerPerfab, new Vector3(2, -0.5f, groundArray.GetLength(0) / 2 + 1), Quaternion.identity, creatureSet.transform);
    }

    private void createEnemyAndGround()
    {
        for (int i = 0; i < groundArray.GetLength(0); i++)
        {
            for (int j = 0; j < groundArray.GetLength(1); j++)
            {
                switch (groundArray[i, j])
                {
                    case 1:
                        Instantiate(planePerfab, new Vector3(j, -0.5f, i), Quaternion.identity, groundSet.transform);
                        break;
                    case 2:
                        Instantiate(cubePerfab, new Vector3(j, 0, i), Quaternion.identity, groundSet.transform);
                        break;
                    case 3:
                        Instantiate(diamondoPerfab, new Vector3(j, 0.4f, i), Quaternion.identity, groundSet.transform);
                        Instantiate(planePerfab, new Vector3(j, -0.5f, i), Quaternion.identity, groundSet.transform);
                        //Instantiate(trailPerfab, new Vector3(j, -0.5f, i), Quaternion.identity, groundSet.transform);
                        break;
                    case 4:
                        Instantiate(planePerfab, new Vector3(j, -0.5f, i), Quaternion.identity, groundSet.transform);
                        Instantiate(TrapPrefab, new Vector3(j, 0f, i), Quaternion.identity, trapSet.transform);
                        break;
                    case 9:
                        Instantiate(planePerfab, new Vector3(j, -0.5f, i), Quaternion.identity, groundSet.transform);
                        Instantiate(enemyPerfab, new Vector3(j, -1.75f, i), Quaternion.identity, enemySet.transform);
                        break;
                    default: break;
                }
            }
        }
    }

    private void setCamera()
    {
        mainCameraObject.transform.position = player.transform.position + Vector3.up * mainCameraUp + Vector3.back * mainCameraBack;
        mainCameraObject.transform.LookAt(player.transform);

        mapCameraObject.transform.position = player.transform.position + Vector3.up * mapCameraUp;
        mapCameraObject.transform.LookAt(player.transform);
    }
